#include <iostream>
using namespace std;

 int main()
 // input to find even or odd number using if and else condition
 {
 	int number;

 	cout << "Enter integer";
 	cin >> number;
 	if( number%2==0 )
 {
 	   cout << "Even number" << number;
 }
 	else 
 	{
 	    cout << "Odd number" << number;
 }
 
 	return 0;
 }
 
